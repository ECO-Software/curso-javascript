/********************
 *    Objeto Math   *
 ********************/
// Mostrar el valor de PI
console.log(Math.PI); // imprime el resultado

// Mostrar dos decimales
console.log(Math.PI.toFixed(2)); // imprime el resultado

// Quitar decimales
console.log(Math.floor(Math.PI)); // imprime el resultado
console.log(Math.floor(5.6)); // imprime el resultado

// Redondear
console.log(Math.round(4.8)); // imprime el resultado
console.log(Math.round(Math.PI)); // imprime el resultado

// Valor absoluto
console.log(Math.abs(-4.8)); // imprime el resultado

// Potencia
console.log(Math.pow(2, 3)); // imprime el resultado

// Numero aleatorio
// No incluye 1 (0 a 0.99999...)
console.log(Math.random()); // imprime el resultado
// Numero aleatorio entre 1 y 10
console.log(Math.floor(Math.random() * 10 + 1)); // imprime el resultado